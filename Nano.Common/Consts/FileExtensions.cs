namespace Nano.Common.Consts;

/// <summary>
/// Common file extension constants.
/// All values are lowercase and include the leading dot.
/// </summary>
public static class FileExtensions
{
    /// <summary>
    /// JPEG image file extension (<c>.jpg</c>).
    /// </summary>
    public const string JPG = ".jpg";

    /// <summary>
    /// JPEG image file extension (<c>.jpeg</c>).
    /// </summary>
    public const string JPEG = ".jpeg";

    /// <summary>
    /// PNG image file extension (<c>.png</c>).
    /// </summary>
    public const string PNG = ".png";

    /// <summary>
    /// Microsoft Word document file extension (<c>.doc</c>).
    /// </summary>
    public const string DOC = ".doc";

    /// <summary>
    /// Microsoft Word Open XML document file extension (<c>.docx</c>).
    /// </summary>
    public const string DOCX = ".docx";

    /// <summary>
    /// HTML document file extension (<c>.html</c>).
    /// </summary>
    public const string HTML = ".html";

    /// <summary>
    /// JSON document file extension (<c>.json</c>).
    /// </summary>
    public const string JSON = ".json";

    /// <summary>
    /// Markdown document file extension (<c>.md</c>).
    /// </summary>
    public const string MD = ".md";

    /// <summary>
    /// PDF document file extension (<c>.pdf</c>).
    /// </summary>
    public const string PDF = ".pdf";

    /// <summary>
    /// Microsoft PowerPoint Open XML presentation file extension (<c>.pptx</c>).
    /// </summary>
    public const string PPTX = ".pptx";

    /// <summary>
    /// Plain text file extension (<c>.txt</c>).
    /// </summary>
    public const string TXT = ".txt";

    /// <summary>
    /// Microsoft Excel Open XML spreadsheet file extension (<c>.xlsx</c>).
    /// </summary>
    public const string XLSX = ".xlsx";
}